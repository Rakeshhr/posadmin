package com.cruds.pos.formbean;

public class L3FormBean {
	
	private String l3MenuName;
	private Double price;
	private Long taxId;
	
	public String getL3MenuName() {
		return l3MenuName;
	}
	public void setL3MenuName(String l3MenuName) {
		this.l3MenuName = l3MenuName;
	}
	public Double getPrice() {
		return price;
	}
	public void setPrice(Double price) {
		this.price = price;
	}
	public Long getTaxId() {
		return taxId;
	}
	public void setTaxId(Long taxId) {
		this.taxId = taxId;
	}

	
}
